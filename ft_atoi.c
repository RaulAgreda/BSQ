int	ft_isspace(char c)
{
	return ((c == ' ' || c == '\n' || c == '\t'
			|| c == '\f' || c == '\v' || c == '\r'));
}

int	ft_atoi(char *str, int size)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (ft_isspace(str[i]))
		i++;
	while (str[i] >= '0' && str[i] <= '9' && i < size)
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	return (n);
}
