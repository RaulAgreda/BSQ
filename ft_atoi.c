int	ft_atoi(char *str, int size)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (str[i] >= '0' && str[i] <= '9' && i < size)
	{
		n = n * 10 + str[i] - '0';
		i++;
	}
	if (i < size)
		return (-1);
	return (n);
}
